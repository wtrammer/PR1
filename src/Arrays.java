
public class Arrays {

	public static void main(String[] args) {

		int[] arr1 = new int[3];
		int[] arr2 = new int[3];
		
		arr1[0] = 3;
		arr1[1] = 4;
		arr1[2] = 5;
		
		arr2[0] = 7;
		arr2[1] = 1;
		arr2[2] = 3;
		
		System.out.println("+" 
								+(arr1[0] + arr2[0])+","
								+(arr1[1] + arr2[1])+","
								+(arr1[2] + arr2[2]));
		
		System.out.println("x" 
								+(arr1[0] * arr2[0])+","
								+(arr1[1] * arr2[1])+","
								+(arr1[2] * arr2[2]));
		
		
		
	}

}
